# 设计类
class Student:
    name = None
    gender = None
    nationality = None
    native_place = None
    age = None


# 创建类
stu_1 = Student()
# 对象属性赋值
stu_1.name = "叽叽哥"
stu_1.gender = "男"
stu_1.nationality = "中国"
stu_1.native_place = "四川省"
stu_1.age = 18
# 获取信息
print(stu_1.name)
print(stu_1.gender)
print(stu_1.nationality)
print(stu_1.native_place)
print(stu_1.age)
